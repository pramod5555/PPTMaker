def generate_slide(brief: str, refs: list[dict], idx: int, total: int) -> str:
    print(f"  [{idx+1}/{total}] generating ({len(refs)} refs) ...", end=" ", flush=True)

    # Build multimodal user message:
    # 1. Reference PNGs as vision images (model sees the actual rendered slides)
    # 2. Reference HTML source (model sees the CSS/layout code)
    # 3. The brief
    content: list[dict] = []

    for i, ref in enumerate(refs):
        if ref.get("b64"):
            content.append({
                "type": "image_url",
                "image_url": {"url": f"data:image/png;base64,{ref['b64']}"},
            })
        ref_label = f"STYLE REFERENCE {i+1} OF {len(refs)}" if len(refs) > 1 else "STYLE REFERENCE"
        content.append({
            "type": "text",
            "text": (
                f"{ref_label} (slide_id: {ref['sid']}):\n"
                f"The image above shows exactly how this reference slide looks rendered. "
                f"Study its color palette, typography, spacing, header treatment, and visual density. "
                f"HTML source for CSS pattern reference:\n{ref['html'][:6000]}\n"
            ),
        })

    content.append({
        "type": "text",
        "text": (
            f"Now create a brand-new {W}×{H}px HTML/CSS presentation slide.\n\n"
            f"REQUIREMENTS:\n"
            f"- Inherit the visual language from the reference slides above: exact color tokens, "
            f"font choices, spacing rhythm, header rule treatment, typography scale\n"
            f"- Do NOT copy content or layout structure — generate fresh content for the brief below\n"
            f"- Match the visual sophistication and density of the references\n\n"
            f"SLIDE BRIEF:\n{brief}"
        ),
    })

    resp = client.chat.completions.create(
        model="gpt-5.4",
        messages=[
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user",   "content": content},
        ],
        max_completion_tokens=8192,
    )

    html = resp.choices[0].message.content or ""
    m = re.search(r"```(?:html)?\s*(<!DOCTYPE.*?</html>)\s*```", html, re.DOTALL | re.IGNORECASE)
    if m:
        html = m.group(1).strip()
    else:
        m = re.search(r"(<!DOCTYPE.*?</html>)", html, re.DOTALL | re.IGNORECASE)
        if m:
            html = m.group(1).strip()

    truncated = "</html>" not in html.lower()
    print("OK" + (" [TRUNCATED]" if truncated else ""))
    return html


