def main() -> None:
    parser = argparse.ArgumentParser()
    parser.add_argument("--topic", default="Digital Megatrends 2025–2030: Five Forces Reshaping Industry")
    parser.add_argument("--out",   default=str(ROOT / "full_deck.html"))
    args = parser.parse_args()

    out_path = Path(args.out)
    slide_specs = build_slides(args.topic)
    total = len(slide_specs)

    print(f"Generating {total}-slide deck: {args.topic}")
    print(f"Output: {out_path}\n")

    slides_html: list[str] = []
    for i, spec in enumerate(slide_specs):
        refs = collect_refs(spec["ref_keys"], max_refs=3)
        html = generate_slide(spec["brief"], refs, i, total)
        slides_html.append(html)
        if i < total - 1:
            time.sleep(0.8)

    deck = wrap_deck(slides_html, args.topic)
    # Encode all non-ASCII as numeric XML entities to prevent mojibake on any platform
    deck_ascii = deck.encode("ascii", errors="xmlcharrefreplace").decode("ascii")
    out_path.write_text(deck_ascii, encoding="ascii")
    print(f"\nDone — {out_path}")
    print(f"Open in browser: file:///{out_path.as_posix()}")


if __name__ == "__main__":
    main()
