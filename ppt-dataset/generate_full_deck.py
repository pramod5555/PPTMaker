SLIDES_DIR = Path(__file__).parent / "slides"


def _encode_png(slide_id: str) -> str | None:
    """Base64-encode the reference PNG for vision input."""
    p = SLIDES_DIR / f"{slide_id}.png"
    if not p.exists():
        return None
    with open(p, "rb") as f:
        return base64.standard_b64encode(f.read()).decode("utf-8")


def collect_refs(keys: list[str], max_refs: int = 3) -> list[dict]:
    """
    Return up to max_refs reference slides, each as a dict with:
      - "html": the HTML source code
      - "b64": base64 PNG (or None if not found)
      - "sid": slide_id

    Multiple references give the model a richer design vocabulary
    rather than anchoring to a single slide's style.
    """
    refs = []
    for k in keys:
        sid = REFS.get(k, "")
        if not sid:
            continue
        p = HTML_DIR / f"{sid}.html"
        if not p.exists():
            continue
        refs.append({
            "sid": sid,
            "html": p.read_text(encoding="utf-8", errors="ignore"),
            "b64":  _encode_png(sid),
        })
        if len(refs) >= max_refs:
            break
    # Fallback: use first HTML file available
    if not refs:
        files = sorted(HTML_DIR.glob("*.html"))
        if files:
            sid = files[0].stem
            refs.append({
                "sid":  sid,
                "html": files[0].read_text(encoding="utf-8", errors="ignore"),
                "b64":  _encode_png(sid),
            })
    return refs


